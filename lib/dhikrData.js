// --- Daily Dhikr ---
export function getDailyDhikr() {
  const today = Math.floor(new Date().getTime() / 1000 / 86400);
  const dhikrIndex = today % PRESET_DHIKRS.length;
  return PRESET_DHIKRS[dhikrIndex];
}